<article id="CRESS">
	<h1>La Chambre Régionale de l’Economie Sociale et Solidaire du Limousin (CRESS)</h1>
	<a href="http://www.creslimousin.org/" target="_blank">
		<img src="./image/cress_limousin.gif" id="logoCRESS" class="logoPartenaires">
	</a>
	<p>Réunis en associations, regroupés en coopératives ou en mutuelles,depuis plus de deux siècles, sous des formes différentes, toujours portés par un idéal humaniste et des valeurs de soldarité, <em>les Limousins ont non seulement été des précurseurs et des pionniers</em> mais ont également participé brillamment à cette histoire en devenir: <em>une économie au service de l'Homme</em>.</p>
	<p>La Chambre Régionale de l’Economie Sociale et Solidaire du Limousin (CRESS) est une association sans but lucratif créée en 2001 dont l’objet est de promouvoir l’économie sociale et solidaire en Limousin.statuts Historiquement la CRESS a pour ancêtre le Groupement Régional des Coopératives (GRC) fondé en 1969 auquel s’est rajoutée la famille des mutuelles de santé et d’assurances pour former le GRCM. En 1987, la famille associative rejoint le GRCM pour former le GRCMA qui en 1995 devient le GRCMA-CRES.</p>
</article>

<article id="IHSCGT">
	<h1>L'Institut d'Histoire Sociale CGT Limoges</h1>
	<a href="http://www.ihs.cgt.fr/" target="_blank">
		<img src="http://www.irhs-midi-pyrenees.com/img/data/logo_phototheque_ihs.jpg" id="logoCRESS" class="logoPartenaires">
	</a>
	<p>L’Institut CGT d’histoire sociale est une association créée en 1982 avec le soutien de la Confédération générale du travail.</p>

	<p>L’Institut mobilise ses atouts et ses compétences pour donner à connaître aux salariés l’histoire sociale et singulièrement la longue expérience de la CGT.</p>

	<p>Son action est diversifiée. En premier lieu l’Institut se consacre à la sauvegarde, au classement et à la valorisation des archives de la Confédération. Cela le conduit à ouvrir les archives à l’étude et à la recherche ainsi qu’à organiser des conférences, séminaires et colloques seul ou en coopération avec des universitaires et des chercheurs.</p>
</article>

<article id="CCFCC">
	<h1>Le Crédit Coopératif et la Fondation du Crédit Coopératif</h1>
	<a href="http://www.credit-cooperatif.coop/fondation/" target="_blank">
		<img src="./image/logo_credit_cooperation.gif" id="logoCCFCC" class="logoPartenaires">
	</a>
	<p>Depuis 30 ans, la Fondation Crédit Coopératif se consacre au soutien d’une économie sociale sans rivages, c'est-à-dire ouverte sur le monde et les innovations.</p>

	<p>Parce que l’économie sociale est composée d’acteurs multiples et a un périmètre d’action très étendu, la Fondation met en œuvre des partenariats avec les mouvements de l’économie sociale dans de nombreux domaines : cohésion sociale, accès à la citoyenneté des personnes handicapées par le sport et la culture, développement durable, etc.</p>

	<p>Parallèlement, la Fondation soutient activement la recherche en économie sociale. Par de nouveaux partenariats significatifs avec des pôles de recherche universitaires, les actions de la Fondation visent à établir un pont entre le fruit du travail de laboratoires de recherche reconnus, d'une part, et les pratiques des acteurs et dirigeants de l'économie sociale d'autre part.
	</p>
</article>

<article id="FNMF">
	<h1>FNMF</h1>
	<a href="http://www.limousin.mutualite.fr/" target="_blank">
		<img src="http://www.ceges.org/images/stories/fnmf.gif" id="logoFNMF" class="logoPartenaires">
	</a>
	<p>La Mutualité Française rassemble 500 mutuelles santé qui protègent 38 millions de personnes. Les mutuelles sont des organismes à but non lucratif. Elles font vivre un système de solidarité, d'entraide et de prévoyance. Elles combattent l'exclusion et la discrimination.</p>
	</p>
	<p>La Mutualité Française Limousin est l'Union régionale de la Mutualité Française dans la région.</p>

	<p>Premier mouvement social du pays, la Mutualité Française fédère 95 % des mutuelles santé en France. Au total, 38 millions de personnes – plus d'un Français sur deux – sont protégées par une mutuelle de la Mutualité Française.</p>

	<p>Acteur incontournable de la politique de santé en France, la Mutualité Française participe activement au maintien d'une protection sociale solidaire et à l'évolution de notre système de santé.</p>
</article>
